                        <h4>Spruce up your garden this autumn</h4>
                        <time datetime="2021-03-20T08:00+08:00">8:00am, 20 March 2021</time>

                        <p><strong>Up your autumn gardening game with these seasonal gardening tips that will have your yard
                                coming up roses.</strong></p>

                        <p>Summer has been and gone, and we say hello to autumn, when the leaves begin to develop those
                            signature tints. The start of the new season might have you wondering: ‘what jobs should I
                            be looking to tick off at home?’</p>

                        <p>Gardening is a great place to start. Here’s what our friends from the Botanic Gardens of
                            South Australia recommend for a season that some say is the most important of the year for
                            your garden:</p>

                        <h5>1. Plant, plant, plant</h5>
                        <p>Autumn’s the ideal time of year to plant – particularly trees, shrubs and perennials –
                            because air temperatures have cooled, soil is still warm and you’ve hopefully had some
                            rainfall to increase soil moisture.</p>

                        <p>The first thing to do is consider the state of your soil and undertake any soil improvements
                            required, such as mixing in soil conditioners, prior to planting.</p>

                        <p>When the soil’s warm and moist, new plantings will establish good root growth before slowing
                            down in winter.</p>

                        <p>You’ll see benefits again in early spring, when the plants you planted in autumn have had
                            time to establish and show wonderful new growth ahead of the next summer’s heat.</p>

                        <p>Autumn is the best time to begin transplanting shrubs or trees, and it is also a great time
                            to propagate new plants from cuttings.</p>

                        <p>Take 10-centimetre cuttings from hardwood herbs such as rosemary and bay, or natives such as
                            banksias, grevillea and coastal rosemary.</p>

                        <p>Remove the lower leaves, dip cuttings into the appropriate hardwood hormone powder and pot
                            them in small containers of free-draining potting mix.</p>

                        <p>Keep the cuttings just moist and shelter them from the direct sun and out of the wind – you
                            can use a plastic bag supported by wire. By spring, you should have rooted cuttings ready to
                            pot up.</p>

                        <h5>2. Plan your veggie garden</h5>
                        <p>Start forward planning and planting now for your winter crops to ensure a bumper harvest.</p>

                        <p>There are lots of vegetables that can be planted in autumn. Try to get all brassicas, such as
                            cabbage, kale, Asian greens, broccoli and cauliflower, in by the start of April.</p>

                        <p>Take a leaf out of the Botanic Gardens’ winter crop for the Little Sprouts Kitchen Garden,
                            which includes beetroot, broad bean, broccoli, coriander, cabbage, celery, fennel, kohlrabi,
                            lettuce, parsnip, snow and sugar snaps peas, silverbeet, swede, spring onion and turnip.</p>

                        <h5>3. Fertilise</h5>
                        <p>Choose a well-balanced fertiliser – one that has equal ratios of nitrogen, phosphorus and
                            potassium, and contains calcium.</p>

                        <p>This will encourage plant cells to thicken, making your plants more resilient to fungus and
                            disease during the cold and wet of winter.</p>

                        <h5>4. Look after your lawn</h5>
                        <p>Autumn’s ideal to help your lawn recover from the hot and dry summer, and to prepare it for
                            the wetter and colder months.</p>

                        <p>It’s a good time to fertilise your lawn, but ideally you want a lower nitrogen content
                            fertiliser than what you use in spring and summer.</p>

                        <p>A more evenly balanced nitrogen, phosphorus and potassium fertiliser will help repair damaged
                            areas by promoting new growth. It will also promote new root growth before soil temperatures
                            drop, giving your lawn a head-start for next spring.</p>

                        <p>Remove fallen leaves from your lawn regularly as these will deprive the lawn of light,
                            causing it to die off and create brown patches.</p>

                        <h5>5. Focus on roses</h5>
                        <p>For rose aficionados, early autumn’s the time to fertilise to ensure your roses have a good
                            supply for that final specky autumn flush.</p>

                        <h5>6. Attract worms</h5>
                        <p>Earthworms are a sign your soil is fertile. When you add organic matter such as leaves and
                            cow manure to your garden soil, you'll attract earthworms, so there's no need to add more to
                            your garden.</p>

                        <p>The worms you’ve attracted with organic matter will add nutrients from their castings, and
                            make tunnels.</p>

                        <p>Check for borer damage on all deciduous trees, paying attention to the trunk at soil level.
                            It‘s easier to check when trees are dormant and bare.</p>

                        <h5>7. Load up your leaves</h5>
                        <p>This time of year produces a lot of leaf matter – why not use this to start a compost of
                            leaves?</p>

                        <p>Traditionally, ‘oak leaf mould’ was an integral part of potting mixes, but more recently it
                            has been replaced by ‘coir’, which comes from the husk of a coconut, or pulverized
                            well-composted pine bark.</p>

